// ================================================================
// ХЕЛПЕР: общий lookup с default-значением
// ================================================================

export function dictLookup(
  dict: Record<string, string>,
  key: string | null | undefined,
  defaultVal: string,
): string {
  if (key == null) return defaultVal;
  const trimmed = key.trim();
  return dict[trimmed] ?? defaultVal;
}
