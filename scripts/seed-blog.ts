async function main() {
  const args = process.argv.slice(2);
  const force = args.includes("--force");

  console.log("=== SEED BLOG START ===");
  if (force) console.log("  [MODE] --force: deleting existing posts first");

  let createdCount = 0;
  let skipCount = 0;
  let deleteCount = 0;
  let errorCount = 0;

  for (const post of POSTS) {
    try {
      // Check if post already exists
      const existing = await api<{ data: Array<{ id: number }> }>(
        `/blogs?filters[slug][$eq]=${post.slug}&pagination[pageSize]=1`,
      );

      if (existing.data?.[0]) {
        if (force) {
          // Delete existing post
          const existingId = existing.data[0].id;
          await api(`/blogs/${existingId}`, { method: "DELETE" });
          console.log(`  [DELETE] ${post.title} (id=${existingId})`);
          deleteCount++;
        } else {
          console.log(`  [SKIP] ${post.title} (slug=${post.slug})`);
          skipCount++;
          continue;
        }
      }

      // Upload images
      const uploadedIds: number[] = [];

      for (const [idx, imgIdx] of post.imageIndexes.entries()) {
        const img = IMAGE_URLS[imgIdx];
        if (!img) continue;

        const ext = getExtension(img.url);
        const filename = `${post.slug}-${idx}.${ext}`;
        const uploaded = await uploadImageFromUrl(img.url, filename);

        if (uploaded) {
          uploadedIds.push(uploaded.id);
          console.log(`  [UPLOAD] ${filename} (fileId=${uploaded.id})`);
        }
      }

      // Create the post (published)
      await api("/blogs", {
        method: "POST",
        body: JSON.stringify({
          data: {
            title: post.title,
            slug: post.slug,
            excerpt: post.excerpt,
            content: post.content,
            author: post.author,
            images: uploadedIds.length > 0 ? uploadedIds : undefined,
            publishedAt: new Date().toISOString(),
          },
        }),
      });

      console.log(`  [CREATE] ${post.title} (slug=${post.slug})`);
      createdCount++;
    } catch (err) {
      console.error(`  [ERROR] Failed to process "${post.title}":`, err);
      errorCount++;
    }
  }

  console.log(`\n=== SEED BLOG COMPLETE ===`);
  console.log(`  Created: ${createdCount}`);
  console.log(`  Skipped: ${skipCount}`);
  console.log(`  Deleted: ${deleteCount}`);
  console.log(`  Errors:  ${errorCount}`);
  console.log(`  Total:   ${POSTS.length}`);
}

main().catch((err) => {
  console.error("Seed failed:", err);
  process.exit(1);
});

