async function api<T = unknown>(
  path: string,
  options?: RequestInit,
): Promise<T> {
  const url = `${STRAPI_URL}/api${path}`;
  const res = await fetch(url, {
    ...options,
    headers: { ...headers, ...((options?.headers || {}) as Record<string, string>) },
  });
  const json = await res.json();
  if (!res.ok) {
    console.error(`[ERROR] ${res.status} ${path}:`, JSON.stringify(json, null, 2));
    throw new Error(`API error ${res.status}: ${json?.error?.message || "Unknown"}`);
  }
  return json as T;
}

async function uploadImageFromUrl(
  imageUrl: string,
  filename: string,
): Promise<{ id: number } | null> {
  // Download the image
  const downloadRes = await fetch(imageUrl);
  if (!downloadRes.ok) {
    console.warn(`  [WARN] Failed to download ${imageUrl} (HTTP ${downloadRes.status}), skipping`);
    return null;
  }

  const blob = await downloadRes.blob();
  const formData = new FormData();
  formData.append("files", blob, filename);

  const uploadRes = await fetch(`${STRAPI_URL}/api/upload`, {
    method: "POST",
    headers: { Authorization: `Bearer ${TOKEN}` },
    body: formData,
  });

  if (!uploadRes.ok) {
    const errText = await uploadRes.text();
    console.warn(`  [WARN] Upload failed for ${filename}: ${errText}, skipping`);
    return null;
  }

  const json = await uploadRes.json();
  return json[0] as { id: number; url: string };
}

function getExtension(url: string): string {
  const match = url.match(/\.(jpg|jpeg|png|gif|webp|svg)(\?|$)/i);
  return match?.[1]?.toLowerCase() || "png";
}

